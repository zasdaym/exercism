// Package reverse is solution for problem Reverse String.
package reverse

// Reverse returns a reversed form of given string.
func Reverse(s string) string {
	chars := []rune(s)
	for i, j := 0, len(chars)-1; i < j; i, j = i+1, j-1 {
		chars[i], chars[j] = chars[j], chars[i]
	}
	return string(chars)
}
